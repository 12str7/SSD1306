namespace System.Device.Gpio
{
    /// <summary>
    /// Represents a value for a pin.
    /// </summary>
    public enum PinValue
    {
        /// <summary>
        /// The value of the pin is low.
        /// </summary>
        Low = 0,
        /// <summary>
        /// The value of the pin is high.
        /// </summary>
        High = 1
    }
}
